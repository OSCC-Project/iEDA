#include "idrc_violation.h"

#include "DRCViolationType.h"

namespace idrc {

/**
 * check if violation is legal,
 * true : legal, ignored this violation
 * false : illegal, need to save this violation
 */
bool DrcViolation::ignored()
{
  if (_violation_type == ViolationEnumType::kDefaultSpacing || _violation_type == ViolationEnumType::kPRLSpacing) {
    if (_net_ids.size() > 0) {
      for (auto net_id : _net_ids) {
        if (net_id >= 0 || net_id == NET_ID_PDN || net_id == NET_ID_VDD || net_id == NET_ID_VSS) {
          return false;  /// need to save
        }
      }

      return true;  /// environment violation, maybe generated by foundry, ignore this violation
    }
  }

  return false;
}

}  // namespace idrc